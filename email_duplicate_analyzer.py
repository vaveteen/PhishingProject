class CampaignDetector:
    """Identifies sender email addresses that belong to the same campaign.
    
    Uses pattern matching to group semantically related addresses such as:
    - nigerianprince03@hotmail.com / algerianprince03@hotmail.com
    - support@bank-secure1.com / support@bank-secure2.com
    """

    
    @staticmethod
    def extract_email_components(email: str) -> Tuple[str, str, str]:
        """Split email into (local_base, numbers, domain)."""
        parts = email.split('@')
        if len(parts) != 2:
            return (email, '', '')
        local, domain = parts
        # Separate numbers from local part
        base = re.sub(r'\d+', '', local)
        numbers = ''.join(re.findall(r'\d+', local))
        return (base, numbers, domain)
    
    @classmethod
    def compute_email_similarity(cls, email_a: str, email_b: str) -> float:
        """Compute similarity between two email addresses."""
        base_a, nums_a, domain_a = cls.extract_email_components(email_a)
        base_b, nums_b, domain_b = cls.extract_email_components(email_b)
        
        # Same domain boost
        domain_sim = 1.0 if domain_a == domain_b else 0.3
        
        # Local part similarity (ignoring numbers)
        if base_a and base_b:
            local_sim = SequenceMatcher(None, base_a, base_b).ratio()
        else:
            local_sim = 0.0
        
        # Combined score
        return (local_sim * 0.6 + domain_sim * 0.4)
    
    @classmethod
    def detect_campaigns(cls, sender_list: List[str], 
                         threshold: float = 0.75) -> List[List[str]]:
        """Group sender emails into campaigns based on similarity."""
        if not sender_list:
            return []
        
        # Deduplicate
        unique_senders = list(set(sender_list))
        n = len(unique_senders)
        
        # Build adjacency (union-find approach)
        parent = list(range(n))
        
        def find(x):
            while parent[x] != x:
                parent[x] = parent[parent[x]]
                x = parent[x]
            return x
        
        def union(x, y):
            px, py = find(x), find(y)
            if px != py:
                parent[px] = py

        
        # Compare all pairs (for reasonable sender counts)
        # For very large sets, use blocking by domain first
        if n > 5000:
            # Block by domain for scalability
            domain_groups = defaultdict(list)
            for i, sender in enumerate(unique_senders):
                domain = sender.split('@')[-1] if '@' in sender else ''
                domain_groups[domain].append(i)
            
            for domain, indices in domain_groups.items():
                for i in range(len(indices)):
                    for j in range(i + 1, len(indices)):
                        sim = cls.compute_email_similarity(
                            unique_senders[indices[i]], 
                            unique_senders[indices[j]]
                        )
                        if sim >= threshold:
                            union(indices[i], indices[j])
        else:
            for i in range(n):
                for j in range(i + 1, n):
                    sim = cls.compute_email_similarity(
                        unique_senders[i], unique_senders[j]
                    )
                    if sim >= threshold:
                        union(i, j)
        
        # Collect groups
        groups = defaultdict(list)
        for i in range(n):
            groups[find(i)].append(unique_senders[i])
        
        # Return groups with more than 1 member
        campaigns = [g for g in groups.values() if len(g) > 1]
        campaigns.sort(key=len, reverse=True)
        return campaigns



