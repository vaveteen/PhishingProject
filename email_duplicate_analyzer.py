class EmailDuplicateAnalyzer:
    """Main analysis engine orchestrating all components.
    
    Pipeline:
    1. Load and parse CSV data
    2. Normalize email content
    3. Compute fingerprints for exact duplicates
    4. Build TF-IDF model for near-duplicate detection
    5. Cluster near-duplicates using similarity threshold
    6. Classify phishing archetypes
    7. Detect sender campaigns
    8. Generate comprehensive report
    """
    
    def __init__(self, threshold: float = 0.90):
        self.threshold = threshold
        self.normalizer = TextNormalizer()
        self.tfidf = TFIDFEngine()
        self.classifier = PhishingArchetypeClassifier()
        self.campaign_detector = CampaignDetector()
        
        # Data storage
        self.raw_emails = []        # Original text
        self.labels = []            # BENIGN/PHISHING
        self.subjects = []          # Extracted subjects
        self.normalized = []        # Normalized text
        self.fingerprints = []      # MD5 fingerprints
        self.tfidf_vectors = []     # TF-IDF sparse vectors
        self.all_senders = []       # All extracted senders
        self.all_urls = []          # All extracted URLs
        
        # Results
        self.duplicate_groups = []   # Groups of duplicate indices
        self.archetype_results = []  # Per-email archetype classification
        self.campaigns = []          # Detected sender campaigns

    
    def load_csv(self, filepath: str) -> int:
        """Load and parse the training set CSV.
        
        Handles the 2-column format: "LABEL","email_content"
        Returns number of emails loaded.
        """
        print(f"[INFO] Loading dataset from: {filepath}")
        start = time.time()
        
        with open(filepath, 'r', encoding='utf-8', errors='replace') as f:
            reader = csv.reader(f, quotechar='"', quoting=csv.QUOTE_ALL)
            for row_num, row in enumerate(reader, 1):
                if len(row) >= 2:
                    label = row[0].strip().upper()
                    content = row[1].strip()
                    
                    if label in ('BENIGN', 'PHISHING'):
                        self.labels.append(label)
                        self.raw_emails.append(content)
                    elif row_num == 1:
                        # Might be a header row - check
                        if 'label' in label.lower() or 'type' in label.lower():
                            continue  # Skip header
                        else:
                            self.labels.append(label)
                            self.raw_emails.append(content)
                elif len(row) == 1 and row_num == 1:
                    # Possible header
                    continue
        
        elapsed = time.time() - start
        count = len(self.raw_emails)
        print(f"[INFO] Loaded {count:,} emails in {elapsed:.2f}s")
        print(f"[INFO] Labels: BENIGN={self.labels.count('BENIGN'):,}, "
              f"PHISHING={self.labels.count('PHISHING'):,}")
        return count

    
    def preprocess(self):
        """Normalize all emails and extract metadata."""
        print("[INFO] Preprocessing and normalizing emails...")
        start = time.time()
        
        for i, raw in enumerate(self.raw_emails):
            # Extract subject (first line)
            subject = self.normalizer.extract_subject(raw)
            self.subjects.append(subject)
            
            # Normalize full text
            norm = self.normalizer.normalize(raw)
            self.normalized.append(norm)
            
            # Fingerprint
            fp = hashlib.md5(norm.encode('utf-8')).hexdigest()
            self.fingerprints.append(fp)
            
            # Extract senders and URLs
            senders = self.normalizer.extract_sender_emails(raw)
            self.all_senders.append(senders)
            
            urls = self.normalizer.extract_urls(raw)
            self.all_urls.append(urls)
            
            if (i + 1) % 5000 == 0:
                print(f"  ... processed {i+1:,}/{len(self.raw_emails):,}")
        
        elapsed = time.time() - start
        print(f"[INFO] Preprocessing complete in {elapsed:.2f}s")
    
    def find_exact_duplicates(self) -> Dict[str, List[int]]:
        """Find exact duplicates using fingerprints."""
        print("[INFO] Finding exact duplicates via fingerprinting...")
        
        fp_groups = defaultdict(list)
        for idx, fp in enumerate(self.fingerprints):
            fp_groups[fp].append(idx)
        
        # Only keep groups with duplicates
        exact_dupes = {fp: indices for fp, indices in fp_groups.items() 
                      if len(indices) > 1}
        
        total_exact = sum(len(v) for v in exact_dupes.values())
        print(f"[INFO] Found {len(exact_dupes):,} exact duplicate groups "
              f"({total_exact:,} emails)")
        return exact_dupes

    
    def find_near_duplicates(self) -> List[List[int]]:
        """Find near-duplicates using TF-IDF cosine similarity.
        
        Uses a two-phase approach for scalability:
        1. Group by fingerprint (exact matches) 
        2. For unique fingerprints, compare TF-IDF vectors
        
        For datasets >10k emails, uses blocking by subject similarity
        to reduce O(n^2) comparisons.
        """
        print(f"[INFO] Finding near-duplicates (threshold={self.threshold})...")
        start = time.time()
        
        # Get unique representatives (one per fingerprint group)
        fp_to_representative = {}
        for idx, fp in enumerate(self.fingerprints):
            if fp not in fp_to_representative:
                fp_to_representative[fp] = idx
        
        unique_indices = list(fp_to_representative.values())
        unique_texts = [self.normalized[i] for i in unique_indices]
        
        print(f"[INFO] Building TF-IDF model on {len(unique_texts):,} "
              f"unique documents...")
        
        # Fit TF-IDF
        self.tfidf.fit(unique_texts)
        
        # Transform all unique docs
        vectors = []
        for text in unique_texts:
            vectors.append(self.tfidf.transform(text))
        
        print(f"[INFO] Vocabulary size: {len(self.tfidf.vocabulary):,} terms")
        print(f"[INFO] Computing pairwise similarities...")
        
        # Union-Find for clustering
        n = len(unique_indices)
        parent = list(range(n))
        
        def find(x):
            while parent[x] != x:
                parent[x] = parent[parent[x]]
                x = parent[x]
            return x
        
        def union(x, y):
            px, py = find(x), find(y)
            if px != py:
                parent[px] = py

        
        # For scalability: use subject-based blocking for large datasets
        comparisons = 0
        if n > 2000:
            # Block by first 3 words of normalized subject
            blocks = defaultdict(list)
            for i, idx in enumerate(unique_indices):
                subj_words = self.normalized[idx][:100].split()[:3]
                block_key = ' '.join(subj_words[:2]) if subj_words else ''
                blocks[block_key].append(i)
            
            # Also create length-based blocks as fallback
            len_blocks = defaultdict(list)
            for i, text in enumerate(unique_texts):
                # Group by text length bucket (within 20% range)
                bucket = len(text) // max(1, len(text) // 5 + 1)
                len_blocks[bucket].append(i)
            
            # Compare within blocks
            for block_indices in blocks.values():
                if len(block_indices) > 500:
                    continue  # Skip overly large blocks
                for i in range(len(block_indices)):
                    for j in range(i + 1, len(block_indices)):
                        idx_i = block_indices[i]
                        idx_j = block_indices[j]
                        sim = TFIDFEngine.cosine_similarity(
                            vectors[idx_i], vectors[idx_j]
                        )
                        comparisons += 1
                        if sim >= self.threshold:
                            union(idx_i, idx_j)
            
            # Also compare within length blocks
            for block_indices in len_blocks.values():
                if len(block_indices) > 300:
                    continue
                for i in range(len(block_indices)):
                    for j in range(i + 1, len(block_indices)):
                        idx_i = block_indices[i]
                        idx_j = block_indices[j]
                        if find(idx_i) == find(idx_j):
                            continue  # Already grouped
                        sim = TFIDFEngine.cosine_similarity(
                            vectors[idx_i], vectors[idx_j]
                        )
                        comparisons += 1
                        if sim >= self.threshold:
                            union(idx_i, idx_j)
        else:
            # Full pairwise comparison for smaller datasets
            for i in range(n):
                for j in range(i + 1, n):
                    sim = TFIDFEngine.cosine_similarity(
                        vectors[i], vectors[j]
                    )
                    comparisons += 1
                    if sim >= self.threshold:
                        union(i, j)
                
                if (i + 1) % 500 == 0:
                    print(f"  ... compared {i+1}/{n} documents")

        
        # Collect clusters (mapping back to original indices)
        clusters = defaultdict(list)
        for i in range(n):
            root = find(i)
            clusters[root].append(unique_indices[i])
        
        # Expand clusters with exact duplicates
        fp_groups = defaultdict(list)
        for idx, fp in enumerate(self.fingerprints):
            fp_groups[fp].append(idx)
        
        expanded_clusters = []
        for cluster_indices in clusters.values():
            full_group = []
            for idx in cluster_indices:
                fp = self.fingerprints[idx]
                full_group.extend(fp_groups[fp])
            # Deduplicate
            full_group = sorted(set(full_group))
            if len(full_group) > 1:
                expanded_clusters.append(full_group)
        
        elapsed = time.time() - start
        print(f"[INFO] Near-duplicate detection complete in {elapsed:.2f}s")
        print(f"[INFO] Performed {comparisons:,} pairwise comparisons")
        print(f"[INFO] Found {len(expanded_clusters):,} duplicate/near-duplicate groups")
        
        self.duplicate_groups = expanded_clusters
        return expanded_clusters
    
    def classify_archetypes(self):
        """Classify all phishing emails into archetypes."""
        print("[INFO] Classifying phishing archetypes...")
        
        for i, raw in enumerate(self.raw_emails):
            if self.labels[i] == 'PHISHING':
                archetypes = self.classifier.classify(raw)
                self.archetype_results.append(archetypes)
            else:
                self.archetype_results.append([])
        
        # Count archetype frequencies
        archetype_counts = Counter()
        for result in self.archetype_results:
            for archetype, confidence in result:
                if confidence >= 0.3:
                    archetype_counts[archetype] += 1
        
        print(f"[INFO] Archetype distribution:")
        for archetype, count in archetype_counts.most_common(10):
            desc = self.classifier.ARCHETYPES[archetype]['description']
            print(f"  {archetype}: {count:,} emails - {desc}")
        
        return archetype_counts

    
    def detect_sender_campaigns(self):
        """Detect sender campaigns from email addresses."""
        print("[INFO] Detecting sender campaigns...")
        
        # Flatten all senders
        all_senders_flat = []
        for senders in self.all_senders:
            all_senders_flat.extend(senders)
        
        # Count sender frequencies
        sender_counts = Counter(all_senders_flat)
        
        # Detect campaigns among frequent senders
        frequent_senders = [s for s, c in sender_counts.items() if c >= 1]
        self.campaigns = self.campaign_detector.detect_campaigns(
            frequent_senders, threshold=0.70
        )
        
        print(f"[INFO] Found {len(self.campaigns)} sender campaigns")
        for i, campaign in enumerate(self.campaigns[:5]):
            print(f"  Campaign {i+1}: {campaign[:5]}"
                  f"{'...' if len(campaign) > 5 else ''}")
        
        return sender_counts
    
    def get_domain_stats(self) -> Counter:
        """Get statistics on sender domains."""
        domain_counts = Counter()
        for senders in self.all_senders:
            for sender in senders:
                if '@' in sender:
                    domain = sender.split('@')[1]
                    domain_counts[domain] += 1
        return domain_counts

    
