# TF-IDF SIMILARITY ENGINE (No External Dependencies)
# =============================================================================

class TFIDFEngine:
    """Lightweight TF-IDF implementation for near-duplicate detection.
    
    Uses cosine similarity on TF-IDF vectors to measure semantic similarity
    between normalized email texts. No external libraries required.
    """
    
    def __init__(self):
        self.vocabulary = {}  # term -> index
        self.idf = {}  # term -> idf value
        self.doc_count = 0
    
    @staticmethod
    def tokenize(text: str) -> List[str]:
        """Simple whitespace + punctuation tokenizer."""
        # Split on non-alphanumeric, keep tokens >= 2 chars
        tokens = re.findall(r'\b[a-z0-9]{2,}\b', text.lower())
        return tokens
    
    def fit(self, documents: List[str]):
        """Build vocabulary and compute IDF from document corpus."""
        self.doc_count = len(documents)
        doc_freq = Counter()
        
        for doc in documents:
            tokens = set(self.tokenize(doc))
            for token in tokens:
                doc_freq[token] += 1
        
        # Build vocabulary (skip very rare and very common terms)
        idx = 0
        for term, freq in doc_freq.items():
            # Skip terms appearing in >95% of docs or only once
            if freq > 1 and freq < self.doc_count * 0.95:
                self.vocabulary[term] = idx
                self.idf[term] = math.log(self.doc_count / (1 + freq))
                idx += 1
    
    def transform(self, text: str) -> Dict[int, float]:
        """Convert text to sparse TF-IDF vector."""
        tokens = self.tokenize(text)
        tf = Counter(tokens)
        total = len(tokens) if tokens else 1
        
        vector = {}
        for term, count in tf.items():
            if term in self.vocabulary:
                idx = self.vocabulary[term]
                tfidf = (count / total) * self.idf[term]
                vector[idx] = tfidf
        return vector

    
    @staticmethod
    def cosine_similarity(vec_a: Dict[int, float], 
                          vec_b: Dict[int, float]) -> float:
        """Compute cosine similarity between two sparse vectors."""
        # Dot product
        dot = 0.0
        for idx, val in vec_a.items():
            if idx in vec_b:
                dot += val * vec_b[idx]
        
        # Magnitudes
        mag_a = math.sqrt(sum(v * v for v in vec_a.values()))
        mag_b = math.sqrt(sum(v * v for v in vec_b.values()))
        
        if mag_a == 0 or mag_b == 0:
            return 0.0
        
        return dot / (mag_a * mag_b)


