class TextNormalizer:
    """Normalizes email text to enable meaningful similarity comparison.
    
    Removes formatting artifacts that do not constitute meaningful content
    differences, including:
    - HTML tags and entities
    - Quoted reply markers (>, >>, etc.)
    - Email headers embedded in body
    - Excessive whitespace and line endings
    - MIME encoding artifacts (=20, =3D, etc.)
    - Signatures and disclaimers
    - Unsubscribe/footer boilerplate
    """
    
    # Patterns for quoted-printable encoding
    QP_PATTERN = re.compile(r'=([0-9A-Fa-f]{2})')
    QP_SOFT_LINEBREAK = re.compile(r'=\s*\n')
    
    # HTML removal
    HTML_TAG_PATTERN = re.compile(r'<[^>]+>')
    HTML_ENTITY_PATTERN = re.compile(r'&[a-zA-Z]+;|&#\d+;')

    
    # Quote markers in replies
    QUOTE_MARKER_PATTERN = re.compile(r'^[>\s]*>', re.MULTILINE)
    
    # Email headers in body
    HEADER_PATTERN = re.compile(
        r'^(From|To|Cc|Bcc|Subject|Date|Sent|Reply-To|'
        r'Content-[Tt]ype|MIME-Version|Message-ID|'
        r'X-[A-Za-z-]+|Return-Path):.*$', re.MULTILINE
    )
    
    # URL pattern
    URL_PATTERN = re.compile(r'https?://[^\s<>"\']+')
    
    # Email address pattern
    EMAIL_PATTERN = re.compile(r'[\w.+-]+@[\w.-]+\.\w+')
    
    # Whitespace normalization
    MULTI_SPACE = re.compile(r'[ \t]+')
    MULTI_NEWLINE = re.compile(r'\n{3,}')
    
    # MIME boundary pattern
    MIME_BOUNDARY = re.compile(r'^--[A-Za-z0-9_=.+-]+\s*$', re.MULTILINE)
    
    # Content-type line pattern
    CONTENT_TYPE_LINE = re.compile(
        r'^Content-type:.*$', re.MULTILINE | re.IGNORECASE
    )

    
    @classmethod
    def normalize(cls, text: str) -> str:
        """Full normalization pipeline for similarity comparison."""
        if not text:
            return ""
        
        # Decode quoted-printable soft line breaks
        text = cls.QP_SOFT_LINEBREAK.sub('', text)
        
        # Decode quoted-printable characters
        def decode_qp(match):
            try:
                return chr(int(match.group(1), 16))
            except (ValueError, OverflowError):
                return match.group(0)
        text = cls.QP_PATTERN.sub(decode_qp, text)
        
        # Remove HTML tags
        text = cls.HTML_TAG_PATTERN.sub(' ', text)
        
        # Decode HTML entities
        text = cls.HTML_ENTITY_PATTERN.sub(' ', text)
        
        # Remove MIME boundaries
        text = cls.MIME_BOUNDARY.sub('', text)
        
        # Remove Content-type lines
        text = cls.CONTENT_TYPE_LINE.sub('', text)
        
        # Remove embedded email headers
        text = cls.HEADER_PATTERN.sub('', text)
        
        # Remove quote markers but keep quoted content
        text = cls.QUOTE_MARKER_PATTERN.sub('', text)
        
        # Normalize whitespace
        text = cls.MULTI_SPACE.sub(' ', text)
        text = cls.MULTI_NEWLINE.sub('\n\n', text)
        
        # Strip lines and remove empty lines
        lines = [line.strip() for line in text.split('\n')]
        lines = [line for line in lines if line]
        text = '\n'.join(lines)
        
        # Lowercase for comparison
        text = text.lower().strip()
        
        return text


    @classmethod
    def extract_subject(cls, raw_text: str) -> str:
        """Extract subject from the first line of email content."""
        if not raw_text:
            return ""
        first_line = raw_text.split('\n')[0].strip()
        # Remove common prefixes
        for prefix in ['Re: ', 'Fwd: ', 'FW: ', 'RE: ']:
            if first_line.startswith(prefix):
                first_line = first_line[len(prefix):]
        return first_line
    
    @classmethod
    def extract_body(cls, raw_text: str) -> str:
        """Extract body (everything after first line)."""
        if not raw_text:
            return ""
        lines = raw_text.split('\n', 1)
        if len(lines) > 1:
            return lines[1]
        return ""
    
    @classmethod
    def extract_sender_emails(cls, raw_text: str) -> List[str]:
        """Extract all email addresses from the text."""
        emails = cls.EMAIL_PATTERN.findall(raw_text)
        # Filter out common non-sender addresses
        filtered = []
        skip_patterns = [
            'unsubscribe', 'privacy', 'noreply', 'no-reply',
            'postmaster', 'mailer-daemon'
        ]
        for email in emails:
            email_lower = email.lower()
            if not any(p in email_lower for p in skip_patterns):
                filtered.append(email_lower)
        return filtered
    
    @classmethod
    def extract_urls(cls, raw_text: str) -> List[str]:
        """Extract all URLs from email text."""
        return cls.URL_PATTERN.findall(raw_text)
    
    @classmethod
    def fingerprint(cls, text: str) -> str:
        """Create a content fingerprint (MD5 hash of normalized text)."""
        normalized = cls.normalize(text)
        return hashlib.md5(normalized.encode('utf-8')).hexdigest()



