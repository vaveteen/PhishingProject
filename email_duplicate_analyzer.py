def main():
    """Command-line interface for the email duplicate analyzer."""
    parser = argparse.ArgumentParser(
        description='Email Duplicate & Near-Duplicate Detection System',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python email_duplicate_analyzer.py training_set.csv
  python email_duplicate_analyzer.py training_set.csv --threshold 0.85
  python email_duplicate_analyzer.py training_set.csv --output my_report.txt

Methodology:
  This tool identifies duplicate and near-duplicate emails using:
  1. Text normalization (removes formatting, HTML, quotes, headers)
  2. MD5 fingerprinting for exact duplicate detection
  3. TF-IDF cosine similarity for near-duplicate detection
  4. Pattern-based phishing archetype classification
  5. Union-find clustering for sender campaign detection

  Duplicates are classified as emails exceeding the similarity threshold
  (default 90%) after content normalization.
        """
    )
    
    parser.add_argument(
        'input_file',
        help='Path to the training_set.csv file'
    )
    parser.add_argument(
        '--threshold', '-t',
        type=float,
        default=0.90,
        help='Similarity threshold for near-duplicates (default: 0.90)'
    )
    parser.add_argument(
        '--output', '-o',
        type=str,
        default='duplicate_analysis_report.txt',
        help='Output report file path (default: duplicate_analysis_report.txt)'
    )
    
    args = parser.parse_args()
    
    # Validate input
    if not os.path.exists(args.input_file):
        print(f"[ERROR] Input file not found: {args.input_file}")
        sys.exit(1)
    
    if not 0 < args.threshold <= 1.0:
        print(f"[ERROR] Threshold must be between 0 and 1.0")
        sys.exit(1)
    
    # Run analysis
    analyzer = EmailDuplicateAnalyzer(threshold=args.threshold)
    analyzer.run_full_analysis(args.input_file, args.output)


if __name__ == '__main__':
    main()
