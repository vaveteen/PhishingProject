    def run_full_analysis(self, csv_path: str, output_path: str):
        """Execute the complete analysis pipeline."""
        print("\n" + "=" * 60)
        print("  EMAIL DUPLICATE & NEAR-DUPLICATE ANALYZER")
        print("=" * 60 + "\n")
        
        total_start = time.time()
        
        # Step 1: Load data
        count = self.load_csv(csv_path)
        if count == 0:
            print("[ERROR] No emails loaded. Check file path and format.")
            sys.exit(1)
        
        # Step 2: Preprocess
        self.preprocess()
        
        # Step 3: Find exact duplicates
        exact_dupes = self.find_exact_duplicates()
        
        # Step 4: Find near-duplicates (includes exact)
        self.find_near_duplicates()
        
        # Step 5: Classify archetypes
        self.classify_archetypes()
        
        # Step 6: Detect campaigns
        self.detect_sender_campaigns()
        
        # Step 7: Generate report
        self.generate_report(output_path)
        
        total_elapsed = time.time() - total_start
        
        # Print summary to console
        total_emails = len(self.raw_emails)
        emails_in_groups = set()
        for group in self.duplicate_groups:
            for idx in group:
                emails_in_groups.add(idx)
        dup_pct = len(emails_in_groups) / total_emails * 100
        
        print(f"\n{'=' * 60}")
        print(f"  ANALYSIS COMPLETE")
        print(f"{'=' * 60}")
        print(f"  Total emails:            {total_emails:,}")
        print(f"  Duplicate groups:        {len(self.duplicate_groups):,}")
        print(f"  Duplication rate:        {dup_pct:.1f}%")
        print(f"  Sender campaigns:        {len(self.campaigns)}")
        print(f"  Total analysis time:     {total_elapsed:.2f}s")
        print(f"  Report saved to:         {output_path}")
        print(f"{'=' * 60}\n")



